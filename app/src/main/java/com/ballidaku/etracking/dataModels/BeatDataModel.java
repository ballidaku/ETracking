public class BeatDataModel
{
    String beatId;
    String beatEmail;
    String beatName;
    String beatPhoneNumber;


    public String getBeatId()
    {
        return beatId;
    }

    public void setBeatId(String beatId)
    {
        this.beatId = beatId;
    }

    public String getBeatEmail()
    {
        return beatEmail;
    }

    public void setBeatEmail(String beatEmail)
    {
        this.beatEmail = beatEmail;
    }

    public String getBeatName()
    {
        return beatName;
    }

    public void setBeatName(String beatName)
    {
        this.beatName = beatName;
    }

    public String getBeatPhoneNumber()
    {
        return beatPhoneNumber;
    }

    public void setBeatPhoneNumber(String beatPhoneNumber)
    {
        this.beatPhoneNumber = beatPhoneNumber;
    }
}
